class Switch
{
public static void main(String args[])
{
int n=5;
switch(n)
{
case 1:System.out.println("Mon");
       break;
case 2:System.out.println("Tue");
       break;
case 3:System.out.println("Wed");
       break;
case 4:System.out.println("Thurs");
       break;
default:System.out.println("Please enter 1 to 4");
       break;
}
}
}